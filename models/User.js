import mongoDB from "mongoose";

const userSchema = new mongoDB.Schema(
  {
    fullName: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    passwordHash: {
      type: String,
      required: true,
    },
    avatarUrl: String,
  },
  { timestamps: true }
);

export default mongoDB.model("User", userSchema);
